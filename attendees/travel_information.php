<div class="SectionHeader"><div class="Full">
<h2><a name="airline">Airline Service</a></h2>
</div></div>
<br/>

<p>Commercial airline service to Dallas is handled by two major airports:
<ul>
<li>Dallas-Fort Worth International Airport (Code: DFW)
 <ul><li><a href="http://www.dfwairport.com">http://www.dfwairport.com</a></li></ul></li>
<li>Dallas Love Field (Code: DAL)
 <ul><li><a href="http://www.dallas-lovefield.com">http://www.dallas-lovefield.com</a></li></ul></li>
</ul>

<p>Most major U.S. airlines fly to DFW, including American Airlines which
uses DFW as a major hub. Southwest Airlines is the major carrier at DAL.</p>

<p>Reserving a flight directly to Dallas will provide the most convenient
access to the conference. Please visit the airport web sites for more
information.</p>

<div class="SectionHeader"><div class="Full">
<h2><a name="shuttle">Ground Transportation</a></h2>
</div></div>
<br/>

<p>Numerous shuttles and taxis travel from both airports to downtown Dallas.
We recommend <a href="http://www.supershuttle.com">SuperShuttle</a> which
currently has rates of $17 one way ($32 round trip) if you book online.
(NOTE: Make sure you select Sheraton Dallas at 400 North Olive Street if
you are staying at the conference hotel. There are 5 Sheraton hotels in
the Dallas metro area.) Look online for discount codes to reduce your fare
to $14 each way.</p>

<p>Visit the airport websites for additional ground transportation options.</p>

<div class="SectionHeader"><div class="Full">
<h2><a name="rentals">Rail Service</a></h2>
</div></div>
<br/>

<p>Within Dallas, the Sheraton Dallas is adjacent to the Red and Blue lines of
the DART Light Rail System. Exit at the Pearl Street station and walk one
block to North Olive Street. Access to the DART Light Rail is available
every day except Sundays via the TRE (Trinity Rail Express) for residents
in the Dallas-Fort Worth metro area. Transfer to the DART at Union
Station. Visit <a href="http://www.dart.org/">DART</a> and <a href="http://www.trinityrailwayexpress.org/">TRE</a> for more information.</p>

<div class="SectionHeader"><div class="Full">
<h2><a name="parking">Driving and Parking at the Sheraton Dallas</a></h2>
</div></div>
<br/>

<p>The Sheraton Dallas is in the heart of downtown Dallas, minutes from the
intersection of interstates 30, 35E, and 45. <em>See map below.</em></p>

<p>Parking at the hotel parking lot costs $19/day for self-parking. There are
a number of surface lots within a few blocks of the hotel that are
significantly cheaper.</p>

<p>Visit the <a href="http://www.starwoodhotels.com/sheraton/property/area/transportation.html?propertyID=3207">Sheraton hotel website</a> for more information on transportation options and parking.</p>

