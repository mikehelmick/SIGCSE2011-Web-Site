<strong>F# - From Foundations to Modern Parallel and Data Rich Functional Programming</strong><br/>
Speakers: Don Syme (Microsoft Research, Cambridge) and Nigel Horspool tbc (University of Victoria)<br/>
<em>Thursday, March 10, 2011 - 10:45am to 12:00pm</em><br/>
&nbsp;<br/>
F# is a multi-paradigm programming language, targeting the .NET/Mono platforms. F# brings you type safe, succinct, efficient and expressive functional programming language supporting both the imperative and object-oriented programming disciplines. It is a simple and pragmatic language, and has particular strengths in data-oriented programming, parallel I/O programming, parallel CPU programming, scripting and algorithmic development. F# combines the advantages of typed functional programming with a high-quality, well-supported modern runtime system and set of libraries. This tutorial will teach F# from several angles: 
<ul>
<li>F# as a cross platform language for teaching, including using F# on Windows, Macs, with Linux and MonoDevelop, and on the TryF# system</li>
<li>The advanced features of F#, including asynchronous and parallel programming and units of measure</li>
<li>F# type providers and their applications to strongly typed programming with web ontologies</li>
<li>F# as a modern data programming environment, including writing data-rich phone applications</li>
</ul>
Attendees should come away with a good idea of the utility and power of F# in the classroom context, and its use all the way up to postgraduate and research level.</p>

<p><strong>XNA Game Studio and PexForFun: Teaching Serious Computer Science and Developing Algorithmic Thinking</strong<br/>
Speakers: Peli de Halleux and Nikolai Tillmann, Microsoft Research, Redmond; Pat Yongpradit<br/>
<em>Friday, March 11, 2011 - 10:45am to 12:00pm</em><br/>&nbsp;<br/>
<em>A Game Programming Framework for the PC, Xbox 360, and Windows Phone 7 Programming Exercises and Automatic Assessment in the Cloud</em><br/>
Delivering  core computer science concepts and promoting deep algorithmic thinking with engaging activities—such as game development—can be a challenge. It can be an additional challenge to infuse game development into an existing CS program with a prescribed curriculum such as Advanced Placement Computer Science. In this presentation educators will preview a 5-week XNA Game Studio mini-course which is easily integrated into an existing curriculum schedule and is designed to engage students in game development, reinforce CS concepts, and develop complex algorithmic skills with the XNA/C# framework. PexForFun is integrated into the mini-course as an additional tool for practice, mastery, and analysis.<br/>&nbsp;
Students in high school through college can use PexForFun as a stand-alone tool to learn and practice programming concepts. With PexForFun, students edit code within any browser; the code is executed and analyzed in the cloud for immediate feedback. PexForFun supports the C#, Visual Basic, and F# programming languages. PexForFun helps students more deeply understand what is happening within the code by using dynamic symbolic execution to thoroughly explore feasible execution paths. The real fun starts with Coding Duels in which students write code that implements a specification and track their progress against the efforts of other PexForFun users. PexForFun connects teachers, curriculum authors, and students in a unique social experience, tracking and streaming progress updates in real time. Try it for yourself at <a href="http://pex4fun.com/">http://pex4fun.com/</a>. Hear about the new XNA mini-course and PexForFun from the curriculum author and the authors of the system at this tutorial.</p>	

<p><strong>Microsoft .NET Gadgeteer: A new way to create electronic devices</strong><br/>
Presenter: Nicolas Villar<br/>
<em>Friday, March 11, 2011 - 3:45pm to 5:00pm</em><br/>&nbsp;<br/>
Getting started building embedded devices is complex involving both programming and electronics skills either limiting the students able to be successful or limiting the complexity of the projects that they can undertake. With the Microsoft .NET Gadgeteer, someone with basic programming skills can be building sophisticated projects in a fraction of the time. <br/>&nbsp;<br/> 
Microsoft .NET Gadgeteer is a toolkit for building small electronic devices built on Microsoft .NET Micro Framework and the Microsoft Visual Studio development environment.  It combines the advantages of object-oriented programming, solderless assembly of electronics with a kit of peripherals, and support for quick physical form factor design using computer-aided design. This powerful combination allows embedded and handheld devices to be iteratively designed, built and programmed in a matter of hours rather than days or weeks. Microsoft .NET Gadgeteer has been developed by Microsoft Research in collaboration with the .NET Micro Framework product team, and is in the process of being made available to the public an open-source project.<br/>&nbsp;<br/>
This session will provide an overview of the various hardware and software elements of the .NET Gadgeteer platform. Attendees will be introduced to the modular electronics system, and learn how individual modules can be connected together to build sophisticated devices. They will learn about how .NET Gadgeteer devices can be programmed easily using the C# language, and interactively debugged Visual Studio IDE. Finally, the session will demonstrate how the toolkit supports the design of custom enclosures for .NET Gadgeteer projects, which can be built on demand by using  3D-printing technologies.</p>

