<h3>TeachScheme!</h3>

<p><em>Thursday, March 10, 2011, 8:30-10:00AM</em></p>

<p><img src="/sigcse2011/attendees/speakers/felleisen.jpg" width="170" align="left" class="headshot" />
	In 1995, my research team and I decided to create TeachScheme!, an
	educational outreach project, with the hope that our work on programming
	languages could effect a dramatic change in K-12 computer science.
	Specifically, we envisioned a virtuous cycle of two mutually reinforcing
	ideas. On the one hand, we would create a design-oriented curriculum path
	from middle school through college. On the other hand, our approach would
	help kids with learning school mathematics. Hence a course on programming
	would benefit every student, not just those who end up choosing computer
	science as a college major. At this point, we have a new design-oriented
	curriculum; a pedagogic program development environment to make it fun;
	and a series of matching programming languages. After focusing at the
	overlap between high schools and colleges at first, we now use
	after-school programs to move upstream, and we are working on two major
	downstream courses for the second semester in college: one on
	object-oriented design and another on logic in program design.<br/>&nbsp;<br/>
	
	My talk will focus on just one aspect of the project: the design-oriented
	curriculum and its smooth path from middle school to college.  I will
	first demonstrate how to teach an intellectually interesting and fun
	course on programming with something that looks like plain school
	mathematics. For the rest of the talk, I will sketch the path from there
	through college.</p>
	
<p><em>Bio:</em> 	Matthias Felleisen obtained his PhD ('87) from Daniel P. Friedman who also
	pointed him in the direction of a professorial career. He then spent the
	next 15 years at Rice University in Houston, including long and short
	sabbaticals at Carnegie Mellon University (Pittsburgh) and Ecole Normale
	Superieure (Paris). In 2001, he took on a position at Northeastern
	University in Boston and moved his entire team there. Felleisen and his
	distributed PLT team conduct research on all aspects of programming
	languages: design, implementations, and applications. On the side, they
	also run TeachScheme!, an educational outreach project.  Over his 25-year
	career, Felleisen co-authored six books. As a PhD student, he revised his
	adviser's "Little LISPer" (MIT Press), which is still in print in its 35th
	 year of existence. The two of them also wrote "A Little Java, A Few
	Patterns". With some of his own PhD students, Felleisen produced "How to
	Design Programs" (MITP, 2001) and "Semantics Engineering" (MITP, 2009).</p>

