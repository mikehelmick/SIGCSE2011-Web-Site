<h3>A Computer Scientist Goes to Washington:  How to be Effective in a World Where Facts are 10% of the Equation</h3>

<p><em>Friday, March 11, 2011, 8:30-10:00AM</em></p>

<p>Government's role in computer science is much larger than funding agencies.   Digital rights management, net neutrality and cybersecurity are hot topics in Washington, hot topics where regulation or legislation may have major impact on the computer systems we develop and enjoy.   Yet the rules governing DC are very different than the rules that govern science and engineering, and learning how to operate in a world where facts are only ten percent of the equation can be a challenging experience for someone more accustomed to proving theorems and building systems.   I'll describe what it takes for a nerd to be effective in the world of government, and give some specific examples in the hot area of cyberwar.</p>

<p>
<img src="/sigcse2011/attendees/speakers/landau.jpg" width="170" align="left" class="headshot" />
Susan Landau's book "Surveillance or Security? The Risks Posed by New
Wiretapping Technologies" (MIT Press) will be appearing in spring
2011, and she will be spending 2010-2011 as a fellow at the Radcliffe
Institute for Advanced Study. From 1999-2010 Landau was a
Distinguished Engineer at Sun Microsystems Laboratories, where she
worked on security, cryptography, and policy, including surveillance
and digital-rights management issues.  Landau is coauthor, with
Whitfield Diffie, of "Privacy on the Line: the Politics of
Wiretapping and Encryption"" (MIT Press, original edition: 1998;
rev. 2007), and author of numerous computer science and public policy
papers, as well as op-eds on cybersecurity and encryption policy.  She
is a member of the National Research Council Computer Science and
Telecommunications Board, serves on the advisory committee for the
National Science Foundation's Directorate for Computer and Information
Science and Engineering, and on the CSIS Commission on Cyber Security
for the 44th Presidency. She received her PhD from MIT, her MS from
Cornell, and her BA from Princeton.</p>



