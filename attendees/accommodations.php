<h2>Why should I stay at the conference hotel?</h2>
</div></div>

<p>If you stay at the conference hotel, you'll be able to network easily with
other conference attendees.  It's a quick trip to get to the program and
functions, and just as quick to zip back to your room for a break.  Plus,
we've negotiated for free Internet at the conference hotel and a
significantly reduced room rate to stay in downtown Dallas. Overall,
you'll have a much better conference experience if you stay at the
conference hotel. Staying at the conference hotel also supports SIGCSE
since we are required to pay for unused rooms in our reserved block which
raises costs for future SIGCSE conferences. So make your conference
experience a better one and help SIGCSE at the same time --- please
reserve your room at the Sheraton Dallas.</p>

<p><em>(If you somehow obtain a better rate online than the one listed on our
site, your stay will still be counted toward our required room block, but
keep in mind that there are often restrictions and/or penalties associated
with these rates. )</em></p>

<p><strong>WIN A FREE UPGRADE</strong>: Every attendee that <em>early register</em> for SIGCSE 2011 at
the regular conference rate and reserves a room at the Sheraton Dallas
will be entered into a drawing to win a complimentary upgrade to a suite.
(Free upgrade drawing not available with exhibits-only, teacher or student
rates.)</p>

            
