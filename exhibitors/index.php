           <p>
            The Association for Computing Machinery
            (<a href="http://www.acm.org">ACM</a>)
            Special Interest Group on Computer Science Education
            (<a href="http://www.sigcse.org">SIGCSE</a>)
            is a community of approximately 2,600 educators and students
            possessing a passion for studying and improving the state
            of computer science education at all levels.
            The annual SIGCSE Technical Symposium is the flagship conference
            of our organization, and is attended by approximately
            1,300 people.  Conference attendees travel from across the globe
            to learn new educational techniques, to examine the latest
            textbooks, to share ideas with colleagues, to talk with
            computing professionals, and to acquire hands-on experience
            with new hardware and software, including software development
            environments, classroom presentation technologies, on-line
            collaboration tools, and robotics.
            </p>
