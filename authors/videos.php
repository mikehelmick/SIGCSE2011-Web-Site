<h2><a name="whatis">What Is The Channel SIGCSE Video Exhibition?</a></h2>
</div></div>

<p>The video exhibition includes videos submitted by you, and your SIGCSE
colleagues that are peer-reviewed. The Video Exhibition appeared at SIGCSE
for the first time in 2009 at Chattanooga. The video exhibition will be
presented in a special "viewing room" at the symposium during SIGCSE 2011.
The video exhibition will also have a home on the web throughout the year.</p>

<div class="SectionHeader"><div class="Full">
<h2><a name="kind">What kind Of Videos Can I Submit?</a></h2>
</div></div>

<p>We invite videos relevant to computer science education. These videos
could include classroom lectures, demonstrations, or animations. The
videos could be useful in today's classroom or be of historical
significance. For SIGCSE 2011, we encourage you to submit a video that
highlights the conference theme.</p>

<p>To be accepted, a video must be:<br />
<ul>
 <li>relevant to computer science educators</li>
 <li>of reasonable quality (<strong>1</strong>) and running time
(<strong>2</strong>)</li>
 <li>submitted with a permission form from the copyright owner (see
permission form below)</li>
</ul>
</p>

<p>To be accepted, a video should:<br />
<ul>
 <li>include captioning</li>
</ul>
</p>

<ol>
 <li>Reasonable quality: There are many aspects of reasonable
quality, both technical and artistic. We won't attempt to list
detailed quality criteria here; however, the panel will know
reasonable quality when they see it. We're not SIGGRAPH and this isn't
Sundance, so flaws are accepted (if not expected). Things to avoid:
periods of non-content, images/scenes with more extraneous items in
view than the focus of the action/story, lengthy credits. You can look
at the <a href="http://www.cs.siue.edu/~dbouvie/channelsigcse/program_summary.htm">2009</a> and <a href="http://www.sigcse.org/sigcse2010/attendees/video_program.php">2010 programs</a> to see examples of acceptable quality.</li>

 <li>Reasonable runtime: While the running time of a video will be a
factor in the selection process, there is no hard limit for the
running time of a video submission. However, you may note that <a
href="http://www.cs.siue.edu/~dbouvie/channelsigcse/program_summary.htm">the 2009 program</a> included videos as long as about 20 minutes
(probably the upper limit) and many that were considerably shorter. As
with any video production, you should strive for maximum impact in the
least amount of time.</li>
</ol>


<div class="SectionHeader"><div class="Full">
<h2><a name="how">How Do I Submit A Video For Exhibition?</a></h2>
</div></div>

<p>Video Submissions are to be provided in PDF format. The submission
contains an up to 800-character (including whitespace) video promotional blurb (description)
which must also be entered into a text box on the submission page. The
procedure for the Video Submission is as follows.</p>

<ol>
  <li>Upload your video to YouTube. If necessary, your video can be
uploaded in parts.</li>

  <li> PDF Video Submission Contents (limited to 1 page)
	<ol>
      <li>Submitter: Include name, affiliation, address, phone and
fax numbers, email address, and WWW address (if applicable). This item
does not count in the page limit. Video Submissions do not undergo
blind review, so this information should be included. You should
expect most correspondence related to the conference to arrive via
email. Please check your spam filter if you haven't received an
expected notification because the auto-responses generated by the
conference software are sometimes blocked.</li>
      <li>Title: Title of the video.</li>
      <li>Approximate runtime: approximate run time of video
[minutes:seconds] (precise times are OK)</li>
      <li>YouTube URLs: the complete link(s) to your video on YouTube</li>
      <li>Promotional Blurb (Description): 800 character (including whitespace) promotion
of the video. Please ensure both submitted copies (here, in the Video
Submission PDF, and in the submission text box) are exactly the same.</li>
    </ol>
  </li>
  <li>Submission text box Video Promotional Blurb (limited to 800 characters, including whitespace):<br/>

     The Video Promotional Blurb is the 'description' of the video
that will appear on the conference web pages and in promotional
material. Your goal in writing a promotional blurb is to attract your
viewing audience. The Video Promotional Blurb must be submitted in
plain text. The promotional blurb for a video may contain a URL where
more information may be found. (Promotional Blurbs of accepted videos
may be edited by the conference committee in response to reviews or
for printed space considerations.)
   </li>
</ol>

<p><strong>Deadlines</strong>: dates are posted at the top of the page.</p>

