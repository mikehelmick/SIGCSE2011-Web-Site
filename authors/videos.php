<h2><a name="if">What If My Video Is Selected For Exhibition?</a></h2>
</div></div>

<p>All submitted videos will be panel-reviewed. For the videos selected for exhibition, the submitters are required to:

<ol>
  <li>Download and complete the permission form (<a href="/sigcse2011/downloads/SIGCSE2011VideoPermissionForm.doc">Word</a> or <a href="/sigcse2011/downloads/SIGCSE201VideoPermissionForm.pdf">PDF</a>). We need a signature, so mail (snail-mail) the completed and signed form to:
  <pre>
      Dennis Bouvier
      SIUE Computer Science
      Box 1656
      Edwardsville, IL 62026 USA
  </pre>
  </li>
  <li>Send us the high-quality version of the video. Snail-mail a (NTSC) DVD disk or mini-DV tape, or send a link to a file (of just about any format); in general, whatever works for you will work for us.</li>
  </ol>
</p>

<p><b>Deadlines:</b> High-quality video must be submitted by the date listed above. All permission forms must be postmarked by the high-quality submission date. SIGCSE is not responsible for permission forms lost in the mail.</p>

