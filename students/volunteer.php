<p>To volunteer you should register to attend the SIGCSE symposium (the registration options are detailed on our <a href="/sigcse2011/attendees/registration.php">SIGCSE 2011 registration page</a>) AND you should <a href="http://www.surveymonkey.com/s/688FTXR">sign up to be a student volunteer</a>.
