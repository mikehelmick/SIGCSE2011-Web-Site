<h1>Guidelines for Reviewers of Posters</h1>


<div class="SectionHeader"><div class="Full">
<h2>General Remarks</h2>
</div></div>

<p>Your job as a reviewer of posters is an important one. Through your reviews you have the opportunity to help the authors and to improve the quality of the symposium. Below are some general guidelines for writing good Poster reviews:</p>
<ul>
   <li> Your job is to write detailed reviews, even for excellent proposals. Tell the authors why you liked their proposal, so that they know what made it successful.</li>
   <li> If you believe that the proposal is poorly written or poorly thought-out, provide CONSTRUCTIVE criticism to help the authors.</li>
   <li> The best reviews clearly justify the reviewer's choice of rating. The least valuable review gives a low score with no written comments. This simply tells the authors that they have been unsuccessful, with no indication of how or why. It is also of no help to the members of the Program Committee, who are charged with making program decisions based on your reviews.</li>
   <li> Posters should provide an opportunity for an INFORMAL presentation featuring "give and take" with conference attendees.  Feedback on whether this seems feasible is helpful.</li>
   <li> While a poster presentation should be sound and well-supported, it need not be complete: a poster presentation should be a good way to discuss and receive feedback on a WORK IN PROGRESS that has NOT BEEN FULLY DEVELOPED into a paper.</li>
</ul>

<div class="SectionHeader"><div class="Full">
<h2>More Detailed Suggestions</h2>
</div></div>

<p>The call for posters suggest they can be used to share teaching materials or present preliminary research findings, and list as possible areas:</p>

<ul>
   <li> imaginative assignments</li>
   <li> innovative curriculum design</li>
   <li> laboratory materials</li>
   <li> effective ideas for recruiting and retaining students</li>
   <li> computing education research that is in a preliminary stage</li>
</ul>

<p>The poster review form is very simple. The benefit of such a review form is that it allows you to write a wide range of comments that are appropriately tailored to the specific proposal.</p>

<p>At the same time, many reviewers appreciate specific suggestions of issues to consider as they read proposals and write reviews. Where appropriate, please try to address the following in your review:</p>

<ul>
   <li> Is the poster topic suitable for the symposium?</li>
   <li> Do you expect that the level of interest in the poster would be high?</li>
   <li> Does the proposal appropriately place the topic in the larger context of Computer Science education?</li>
   <li> Are the authors aware of a range of ideas on the topic?</li>
   <li> If appropriate, do the authors cite related work? (In evaluating this, please keep in mind that proposals are limited to two pages, and that posters represent work in progress, so it is unreasonable to expect a detailed "Related Work" section or a long bibliography.)</li>
   <li> Do the authors clearly state the significance and relevance of their topic?</li>
   <li> Is the proposal well-written?</li>
   <li> Is the content clear and well-organized?</li>
   <li> Are there any technical errors?</li>
   <li> Do you have any suggestions for the authors ...</li>
   <ul>
         <li> ... to improve the proposal itself (content and layout suggestions)?</li>
         <li> ... to improve the quality of the presentation, if accepted?</li>
   </ul>
</ul>

<p>Obviously this list is not exhaustive. The Program Committee and authors will appreciate your views on other issues as well.</p>  
   
   

