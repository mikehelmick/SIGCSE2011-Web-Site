            After reviewing is complete, authors will see their reviews
            as in previous years.
            </p>
 
            <p>Authors will also receive meta-reviews that synthesize the
            existing reviews, clarifying how the program committee
            interprets the disparate information from individual reviews.
            </p>


<div class="SectionHeader"><div class="Full">
<h2>Reviewers</h2>
</div></div>
            <p>
            At least four reviewers review each paper.
            Reviewers are drawn from the SIGCSE reviewer pool
            with preference for those whose expertise match the
            paper's topic areas.
            </p>

            <p>
            A review must objectively, accurately, and clearly assess
            a paper's suitability for publication at SIGCSE,
            founded in the reviewer's disciplinary expertise
            and on the basis of the written paper's originality,
            technical soundness, contribution to computer science
            education, and clarity of presentation.
            </p>

            <p>Reviews that do not meet this standard may be deleted.
            For example, an unacceptable review might:
            </p>

            <ul>
              <li> be incoherent, unreadable, or irrelevant to the paper;
                   </li>

              <li> focus on the paper's topic area or presumed authors
                   at the expense of assessment of the paper itself; or
                   </li>

              <li> provide no justification for its numeric ratings.
                   (Even in &quot;obvious&quot; cases, reviewers should
                   briefly justify ratings.)
                   </li>
            </ul>

            <p>
            Reviewers whose reviews are consistently unacceptable
            may be permanently disbarred from SIGCSE reviewing.
            </p>

            <p>
            Note: A difference in rating or opinion with other reviewers
            or with Program Committee members will <strong>never</strong>
            be cause for deletion of a review.
            </p>


<div class="SectionHeader"><div class="Full">
<h2>Associate Program Chairs</h2>
</div></div>
            <p>One of the six Associate Program Chairs will synthesize
            each paper's reviews into a &quot;meta-review&quot;
            to improve accuracy, quality, and effectiveness
            of the selection process.
            </p>

            <p>
            Associate Program Chairs were selected by the Program Chairs
            for their disciplinary expertise.  Associate Program Chairs
            read the papers' reviews and abstracts though not,
            in most cases, the paper itself.
            </p>

            <p>
            Associate Program Chairs will:
            </p>

            <ol>
              <li> Delete, in the rare cases described above,
                   unacceptable reviews.  
                   </li>

              <li> Usually, summarize the case for and against accepting
                   a paper based on its abstract, reviews, and possibly
                   its contents.

                   <p style="font-size: 1.0em">
                   Sometimes, summarize only the case for accepting
                   if reviewers' ratings are consistently high,
                   or analogously, only the case against accepting when
                   ratings are consistently low.
                   </p>

                   <p style="font-size: 1.0em">
                   In rare cases (e.g., when the reviews are deemed
                   irreconcilably disparate or when too few reviewers
                   claim familiarity with the area), read the paper fully
                   and provide a &quot;normal&quot; review, in addition
                   to the meta-review.
                   </p>
                   </li>

              <li> Rate the paper on the basis of existing reviews.
                   Associate Program Chairs are charged with making strong
                   recommendations; thus, their ratings will often be 1s
                   or 6s.  They will briefly justify the rating in the
                   meta-review.
                   </li>
            </ol>


