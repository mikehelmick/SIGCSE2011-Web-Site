<div class="SectionHeader"><div class="Full">
<h2>Program Chairs</h2>
</div></div>

            <p>
            The Program Chairs' duty, quoted from
            <a href="http://sigcse.org/conferences/ProgramChair.shtml">SIGCSE's Program Chair Responsibility Policy</a>,
            is &quot;to create a good, well balanced program at the SIGCSE
            Technical Symposium and the ITiCSE conference.  The program
            should provide both short and long term benefits
            to the conference attendees and the wider community.&quot;
            </p>

            <p>
            Within the framework described here, that means ensuring
            overall quality in the reviewing process and assembling
            an excellent program that reflects reviewers' and
            Associate Program Chairs' decisions with respect to
            the overall composition, exigencies, and special focus
            of the conference.
            </p>

            <p>
            Program Chairs make all final decisions on accepting or
            rejecting papers on the basis of the papers, their reviews,
            their meta-reviews,
            and a global effort to compose an excellent program.
            </p>

            <p>
            A Program Chair will read every meta-review of every
            submitted paper.  In many cases, Program Chairs will also
            consult abstracts, reviews, or the papers themselves.
            </p>

            <p>
            Finally, the Program Chairs will report to the community
            --- in the proceedings or at the business meeting --- 
            on the aggregate outcome of the process and its
            potential for revision or extension in coming years.
            </p>

