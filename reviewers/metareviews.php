<h1>The Meta-Review Process for Paper Submissions</h1>

            <p>
            SIGCSE has been progressively improving our reviewing process
            in response to community requests, e.g., [1,2,3].
            At the same time, the number of submissions to SIGCSE has risen
            over time.  (Last year, there were 2 Program Chairs,
            324 paper submissions, and over 1500 reviews.)
            </p>

            <p>
            To continue the process of improvement, to provide authors
            with more information on how the Program Chairs use reviews
            to analyze the paper and how authors can use reviews
            constructively in case of resubmission, and to mitigate the
            increasing burden on the Program Committee chairs,
            we have established a prototype paper reviewing process
            for SIGCSE 2009.  This page explains how the new process
            affects each of the constituencies.
            </p>


<div class="SectionHeader"><div class="Full">
<h2>Authors</h2>
</div></div>   
            <p>
            <!--
            Authors submit papers for double-blind review.
            The goal of the anonymized version is to, as much as possible,
            allow the author(s) of the paper an unbiased review.
            The authors choose the level of anonymity in the paper.
            At the lowest level, they may choose to use no anonymization;
            at the highest level, they may remove <strong>all</strong>
            references to the authors (including author's names and
